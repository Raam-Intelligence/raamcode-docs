async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true })
  const files = []
  for (const e of entries) {
    const p = join(dir, e.name)
    if (e.isDirectory()) files.push(...(await walk(p)))
    else if (e.isFile() && p.endsWith('.md')) files.push(relative(DOCS, p))
  }
  return files
}

function stripFrontmatter(md) {
  if (!md.startsWith('---')) return md
  const end = md.indexOf('\n---', 3)
  if (end === -1) return md
  return md.slice(end + 4).replace(/^\n+/, '')
}

async function main() {
  const onDisk = (await walk(DOCS)).sort()
  const missingFromOrder = onDisk.filter((f) => !ORDER.includes(f))
  const missingOnDisk = ORDER.filter((f) => !onDisk.includes(f))

  if (missingOnDisk.length) {
    console.error('Listed in ORDER but missing on disk:', missingOnDisk)
    process.exit(1)
  }
  if (missingFromOrder.length) {
    console.warn('On disk but not in ORDER (will be appended):', missingFromOrder)
  }

  const final = [...ORDER, ...missingFromOrder]

  const header = `# RaamCode — full documentation

This file is the concatenated content of every page on the RaamCode docs site, in the same order as the site navigation. Generated by scripts/build-llms-full.mjs from docs/. The structured index is at /llms.txt.

`

  const sections = []
  for (const rel of final) {
    const full = join(DOCS, rel)
    const raw = await readFile(full, 'utf8')
    const body = stripFrontmatter(raw)
    const urlPath = '/docs/' + rel.replace(/\.md$/, '').replace(/\/index$/, '/')
    sections.push(`<!-- ── ${urlPath} ── -->\n\n${body.trim()}\n`)
  }

  const out = header + sections.join('\n\n---\n\n') + '\n'
  await writeFile(OUT, out, 'utf8')

  const bytes = Buffer.byteLength(out, 'utf8')
  console.log(`Wrote ${OUT} — ${final.length} pages, ${(bytes / 1024).toFixed(1)} KB`)
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
